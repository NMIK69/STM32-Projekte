void unload_capacitor() {

	/*
		This function handels getting the conversion value
		and sending it to the PC while unloading the capacitor
	*/


	uint32_t data = 6; // data read from pin
	uint32_t start_time = HAL_GetTick();
	uint32_t elapsed = 0;
	uint32_t prev_time = 0;

	
	// set charging Pin to LOW
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, GPIO_PIN_RESET);
	// turn of led
	HAL_GPIO_WritePin(GPIOB, CAP_LED_Pin, GPIO_PIN_RESET);


	while(data > 5 && change_mode == 0) {

		elapsed = HAL_GetTick() - start_time; // keep track of the time 
		
		// let LED blink to indicate unloading
		if(prev_time + 200 < elapsed) {
			HAL_GPIO_TogglePin(GPIOB, CAP_LED_Pin);
			prev_time = elapsed;
		}
		
		
		// start the conversion
		HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);


		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, 10);


		data = HAL_ADC_GetValue(&hadc1); // get data


		HAL_ADC_Stop(&hadc1);
		// end the conversion

		send_all_data(elapsed, data, 0); // send the data with UART


		HAL_Delay(1);
	}

	
	if(change_mode == 1) {
		change_mode = 0;
		HAL_GPIO_WritePin(GPIOB, CAP_LED_Pin, GPIO_PIN_RESET); // make sure that the LED is off when switching modes
	}
}


void load_capacitor() {
	/*
		This function handels getting the conversion value
		and sending it to the PC while loading the capacitor
	*/
	
	uint32_t data = 0; // data read from pin
	uint32_t start_time = HAL_GetTick();
	uint32_t elapsed = 0;

	
	// start charging by setting the charging Pin to High
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, GPIO_PIN_SET);


	// wait untill the capacitor is fully charged
	while(data < 4080 && change_mode == 0) {

		elapsed = HAL_GetTick() - start_time;


		HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);


		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, 10);


		data = HAL_ADC_GetValue(&hadc1); // get data


		HAL_ADC_Stop(&hadc1);


		send_all_data(elapsed, data, 1); // send the data with UART


		HAL_Delay(1);
	}

	// turn on the LED to indicate that the capacitor is fully charged
	if(data > 4000) HAL_GPIO_WritePin(GPIOB, CAP_LED_Pin, GPIO_PIN_SET);

	if(change_mode == 1) change_mode = 0;
}


void start() {
	if(mode == 0) {
		load_capacitor();
	}
	if(mode == 1) {
		unload_capacitor();
	}
}

